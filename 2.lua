-- Applies weapon skin by index and refreshes attachment skins in real-time
-- Free publisher @ADITYA
function _G.set_skin_by_index(weapon_id, index)
    if not weapon_id or weapon_id == 0 then return end
    local info = _G.WeaponSkinMap[weapon_id]
    if not info or not info.skins then return end
    local skin_id = info.skins[index]
    if not skin_id then return end

    _G.redirectRules[weapon_id] = skin_id
    _G.redirectRules = _G.expand_redirect(_G.redirectRules)

    local hud = slua_GameFrontendHUD
    local pc = hud and hud:GetPlayerController()
    if not pc then return end
    local char = pc:GetPlayerCharacterSafety()
    if not slua.isValid(char) then return end
    local wm = char:GetWeaponManager()
    if not slua.isValid(wm) then return end

    for slot = 1, 3 do
        local weapon = wm:GetInventoryWeaponByPropSlot(slot)
        if slua.isValid(weapon) and slua.isValid(weapon.synData) then
            if weapon:GetWeaponID() == weapon_id then
                local item_data = weapon.synData:Get(7)
                if item_data and item_data.defineID then
                    item_data.defineID.TypeSpecificID = skin_id
                    weapon.synData:Set(7, item_data)
                    for attach_slot = 0, 4 do
                        local attach_data = weapon.synData:Get(attach_slot)
                        if attach_data and attach_data.defineID then
                            local attach_id = attach_data.defineID.TypeSpecificID
                            if attach_id and attach_id > 0 and attach_id < 10000000 then
                                local new_attach_id = remap_by_base_type(attach_id, skin_id)
                                if new_attach_id ~= attach_id then
                                    attach_data.defineID.TypeSpecificID = new_attach_id
                                    weapon.synData:Set(attach_slot, attach_data)
                                    if _G.download_item then _G.download_item(new_attach_id) end
                                end
                            end
                        end
                    end
                    if weapon.DelayHandleAvatarMeshChanged then
                        weapon:DelayHandleAvatarMeshChanged()
                    end
                    local avatar = weapon.WeaponAvatarComponent_BP or weapon.WeaponAvatarComponent or weapon.WeaponAvatar
                    if slua.isValid(avatar) then
                        if avatar.PutOnEquipmentByResID then avatar:PutOnEquipmentByResID(skin_id, {}) end
                        if avatar.RefreshAvatar then avatar:RefreshAvatar() end
                        if avatar.RebuildAvatar then avatar:RebuildAvatar() end
                    end
                end
                break
            end
        end
    end

    if _G.ApplyOutfit then _G.ApplyOutfit(char) end
end

